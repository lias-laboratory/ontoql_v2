package fr.ensma.lias.mariusql.engine;

import java.io.InputStream;
import java.io.Reader;

import fr.ensma.lias.mariusql.engine.antlr.MariusQLBaseLexer;
import fr.ensma.lias.mariusql.exception.NotYetImplementedException;

/**
 * Custom lexer for the MariusQL grammar. Extends the base lexer generated by
 * ANTLR in order to keep the grammar source file clean.
 * 
 * @author Mickael BARON
 * @author St√©phane JEAN
 */
public class MariusQLLexer extends MariusQLBaseLexer {

	/**
	 * Constructor with an InputStream.
	 * 
	 * @param in an input InputStream
	 */
	public MariusQLLexer(final InputStream in) {
		super(in);
	}

	/**
	 * Constructor with a Reader.
	 * 
	 * @param in input Reader
	 */
	public MariusQLLexer(final Reader in) {
		super(in);
	}

	@Override
	public void setTokenObjectClass(final String cl) {
		// Ignore the token class name parameter, and use a specific token
		// class.
		super.setTokenObjectClass(MariusQLToken.class.getName());
	}

	@Override
	public void panic() {
		panic("CharScanner: panic");
	}

	@Override
	public void panic(final String s) {
		throw new NotYetImplementedException("Replace QueryException of Hibernate to another kind of exception.");
	}
}
